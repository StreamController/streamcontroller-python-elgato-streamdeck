    def _convert_key_num_to_device_key_id(self, key):
        return self.KEY_NUM_TO_DEVICE_KEY_ID[key]
    
    def _convert_device_key_id_to_key_num(self, key):
        return self.KEY_DEVICE_KEY_ID_TO_NUM[key]
    
    def _make_payload_for_report_id(self, report_id, payload_data):
        payload = bytearray(self.PACKET_LENGTH+1)
        payload[0] = report_id
        payload[1:len(payload_data)+1] = payload_data
        return payload

    def _send_keep_alive(self):
        # # connect/ping # CRT\0\0CONNECT
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x00, 0x00, 0x43, 0x4f, 0x4e, 0x4e, 0x45, 0x43, 0x54])
        self.device.write(payload)

    def _read_control_states(self):
        """Requires ~10 second poll for a keep-alive of device
        
        Given the default polling rate of 1kHz, this would be 10k ticks
        
        TODO It would be better to overload _read/_read_from_suspend, as this adds duplicate code.
        There needs to be some cleanup in the _read functions first though."""

        device_input_data = self.device.read(self.PACKET_LENGTH)  # Only read maximum of possible given device

        self.counter += 1

        if self.counter >= self.read_poll_hz*self.NUM_SECONDS_KEEP_ALIVE:
            # print("Sending keep alive")
            self._send_keep_alive()
            self.counter = 0

        if device_input_data is None:
            return None

        # Check that this is a valid message
        # ACK\0\0OK\0
        if(device_input_data.startswith(bytes([0x41, 0x43, 0x4b, 0x00, 0x00, 0x4f, 0x4b, 0x00]))):
            read_event = device_input_data[9]
            read_value = device_input_data[10]
        else:
            # we don't know how to handle the response
            return None
        
        # print(f"Received event from 0x{read_event:0x} : 0x{read_value:0x}")
        
        if read_event in self.KEY_NUM_TO_DEVICE_KEY_ID: # Key Event

            # Save last state to be able to build output array
            self._int_key_states[self.KEY_DEVICE_KEY_ID_TO_NUM[read_event]] = bool(read_value)

            return {
                ControlType.KEY: self._int_key_states
            }
        elif read_event in self.DIAL_MAPPING["push"]: # Dial push

            self._int_dial_key_states[self.DIAL_PUSH_LOOKUP[read_event]] = read_value

            return {
                ControlType.DIAL: {
                    DialEventType.PUSH: self._int_dial_key_states,
                }
            }
        elif read_event in self.DIAL_MAPPING["cw"]: # Dial turn
            # print(f"Handling CW turn")
            states = [0] * self.DIAL_COUNT
            states[self.DIAL_TURN_CW_LOOKUP[read_event]] = -1
            return {
                ControlType.DIAL: {
                    DialEventType.TURN: states,
                }
            }
        elif read_event in self.DIAL_MAPPING["ccw"]: # Dial turn
            # print(f"Handling CCW turn")
            states = [0] * self.DIAL_COUNT
            states[self.DIAL_TURN_CCW_LOOKUP[read_event]] = 1
            return {
                ControlType.DIAL: {
                    DialEventType.TURN: states,
                }
            }
        else:
            print(f"Unknown event for key id 0x{read_event:0x} received")
            return None

    def _reset_key_stream(self):
        self.reset()

    def _send_disconnect(self):
        # disconnect # CRT\0\0DIS
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x00, 0x00, 0x44, 0x49, 0x53])
        self.device.write(payload)

    def initialize(self):

        self.counter = 0
        self._int_key_states = [False] * self.KEY_COUNT
        self._int_dial_key_states = [False] * self.DIAL_COUNT

        self._send_disconnect()
        self.set_brightness(0x19)  # Based on raw captures

        # Quad CMD # CRT\0\0QUCMD
        # From packet capture 43 52 54 00 00 51 55 43 4d 44 11 11 00 11 00 11
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x00, 0x00, 0x51, 0x55, 0x42, 0x4d, 0x44, 0x11, 0x11, 0x00, 0x11, 0x00, 0x11])
        self.device.write(payload)

        self._send_keep_alive()

        self.clear_all_button_images()
        self.clear_button_states()


    def clear_all_button_images(self):
        self.clear_button_image(0xff)

    def clear_button_image(self, key):

        cval = 0xff if key == 0xff else key + 1 

        # clear contents # CRT\0\0CLE #0x00 0x00 0x00 <KEY ID | 0xff for all>
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x00, 0x00, 0x43, 0x4c, 0x45, 0x00, 0x00, 0x00, cval])
        self.device.write(payload)

    def clear_lcd_displays(self):

        # clear contents # CRT\0\0CLE\0DC
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x0, 0x0, 0x43, 0x4c, 0x45, 0x0, 0x44, 0x43])
        self.device.write(payload)

    def clear_button_states(self):

        self._int_key_states = [False] * self.KEY_COUNT
        self._int_dial_key_states = [False] * self.DIAL_COUNT

        # clear contents # CRT\0\0CLB\0DC
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x0, 0x0, 0x43, 0x4c, 0x42, 0x0, 0x44, 0x43])
        self.device.write(payload)

    def halt_device(self):
        # clear contents # CRT\0\0HAH
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x00, 0x00, 0x48, 0x41, 0x48])
        self.device.write(payload)

    def reset(self):
        self.initialize()

        # Set brightness to full
        self.set_brightness(100)

        # Clear all button images
        self.clear_all_button_images()

    def set_brightness(self, percent):
        if isinstance(percent, float):
            percent = int(100.0 * percent)

        percent = min(max(percent, 0), 100)

        # set brightness # CRT\0\0LIG #0x00 0x00 <PERCENT> 0x00
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x00, 0x00, 0x4c, 0x49, 0x47, 0x00, 0x00, percent, 0x00])
        self.device.write(payload)

    def get_serial_number(self):
        return self.device.serial_number()

    def get_firmware_version(self):
        version = self.device.read_input(0x00, self.PACKET_LENGTH + 1)
        return self._extract_string(version[1:])

    def set_key_image(self, key:int, image):
        if min(max(key, 0), self.KEY_COUNT) != key:
            raise IndexError("Invalid key index {}.".format(key))

        image = bytes(image or self.BLANK_KEY_IMAGE)
        image_payload_page_length = self.PACKET_LENGTH

        if key in self.NO_IMG_KEYS:
            raise RuntimeWarning("Requested key index {} has no image.".format(key))
            # TODO determine proper error/warning behavior
            return

        key = self._convert_key_num_to_device_key_id(key)

        image_size_uint16_be = int.to_bytes(len(image), 2, 'big', signed=False)

        # start batch # CRT\0\0BAT #0x00 0x00 <image size uint16_be> <key id>
        command = bytes([0x43, 0x52, 0x54, 0x00, 0x00, 0x42, 0x41, 0x54, 0x00, 0x00]) + image_size_uint16_be +  bytes([key]) 
        payload = self._make_payload_for_report_id(0x00, command)
        self.device.write(payload)

        page_number = 0
        bytes_remaining = len(image)
        while bytes_remaining > 0:
            this_length = min(bytes_remaining, image_payload_page_length)
            bytes_sent = page_number * image_payload_page_length

            #send data
            payload = self._make_payload_for_report_id(0x00, image[bytes_sent:bytes_sent + this_length])
            self.device.write(payload)

            bytes_remaining = bytes_remaining - this_length
            page_number = page_number + 1
        
        # stop batch # CRT\0\0STP
        payload = self._make_payload_for_report_id(0x00, [0x43, 0x52, 0x54, 0x00, 0x00, 0x53, 0x54, 0x50])
        self.device.write(payload)
        

